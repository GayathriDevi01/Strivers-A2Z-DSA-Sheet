//User function Template for Java
class Solution {
    static String armstrongNumber(int n){
        // code here
        int temp=n;
        int sum=0;
        while(n>0){
            int rem=n%10;
            sum+=Math.pow(rem,3);
            n/=10;
        }
        if(sum==temp)
           return "Yes";
        else
           return "No";
    }
}
